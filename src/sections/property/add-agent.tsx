type Props = {
  open: boolean;
  onClose: VoidFunction;
  currentProperty?: IPropertyItem;
};

export default function AddAgent({ currentProperty, open, onClose }: Props) {
  const { enqueueSnackbar } = useSnackbar();

  const {agents, agentsEmpty, agentsLoading} = useGetAgentList(1,1000);

  const NewUserSchema = Yup.object().shape({
    agents : Yup.array().min(1, 'Choose at least one agent'),
  });

  const defaultValues = useMemo(
    () => ({
        agents: agents?.map((agent:any) => agent.id) || [],
    }),
    [agents]
  );

  const methods = useForm({
    resolver: yupResolver(NewUserSchema),
    defaultValues,
  });

  const {
    reset,
    handleSubmit,
    formState: { isSubmitting },
  } = methods;

  const onSubmit = handleSubmit(async (data) => {
    try {
      await new Promise((resolve) => setTimeout(resolve, 500));
      reset();
      onClose();
      enqueueSnackbar('Update success!');
      console.info('DATA', data);
    } catch (error) {
      console.error(error);
    }
  });

  console.log(agents);
  

  return (
    <Dialog
      fullWidth
      maxWidth={false}
      open={open}
      onClose={onClose}
      PaperProps={{
        sx: { maxWidth: 720 },
      }}
    >
      <FormProvider methods={methods} onSubmit={onSubmit}>
        <DialogTitle>Quick Update</DialogTitle>

        <DialogActions>
          <Button variant="outlined" onClick={onClose}>
            Cancel
          </Button>

          <LoadingButton type="submit" variant="contained" loading={isSubmitting}>
            Update
          </LoadingButton>
        </DialogActions>
      </FormProvider>
    </Dialog>
  );
}
